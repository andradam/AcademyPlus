#include <unistd.h>

int	main()
{
	int i;
	char c;

	i = 0;
	c = 'a';
	while (c <= 'z')
	{
		if ((i % 2) == 1)
		{
			c = c - 32;
			write(1, &c, 1);
			c = c + 32;
		}
		else
		write(1, &c, 1);
		i++;
		c++;
	}
	write(1, "\n", 1);
	return (0);
}
